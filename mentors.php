        <script>
          const formListItems = document.querySelectorAll('#formList li');
          const rightPanelTitle = document.querySelector('.right-panel h2');
          const rightPanelDesc = document.querySelector('.right-panel p');
          const downloadButton = document.querySelector('.download-button');
          const certExample = document.querySelector('.certification-example');
          const resumeExample = document.querySelector('.resume-example');
          const signupExample = document.querySelector('.signup-example');
      
          const formDetails = [
            {
              title: 'Resume Form',
              description: 'The Resume Form allows you to submit your updated resume, which includes your academic achievements, extracurricular activities, and other relevant experiences. Make sure to provide accurate and detailed information for future reference.',
            },
            {
              title: 'Certifications',
              description: 'Submit your certifications or official documents to validate any qualifications or accomplishments. This form ensures that your certifications are up-to-date and accurately recorded in your scholar profile.',
            },
            {
              title: 'Complete Sign Up',
              description: 'Complete the sign-up process by providing all necessary personal details, academic records, and additional required information. This form helps finalize your registration and ensures that you are eligible for program participation.',
            }
          ];
      
          let currentIndex = 0;
      
          function updateFormPanel(index) {
            if (index < 0 || index >= formDetails.length) return;
      
            rightPanelTitle.textContent = formDetails[index].title;
            rightPanelDesc.textContent = formDetails[index].description;
      
            formListItems.forEach(el => el.classList.remove('active'));
            formListItems[index].classList.add('active');
      
            // Reset visuals
            downloadButton.style.display = 'inline-block';
            certExample.style.display = 'none';
            resumeExample.style.display = 'none';
            signupExample.style.display = 'none';
      
            // Handle based on form
            if (index === 0) {
              downloadButton.textContent = 'Download Form';
              downloadButton.setAttribute('href', 'uploads/img/CoachResume.docx');
             downloadButton.setAttribute('download', 'Coach Resume.docx');
              resumeExample.style.display = 'block';
            } else if (index === 1) {
              downloadButton.textContent = 'Download Examples';
              downloadButton.setAttribute('href', 'uploads/img/CertificationsExample.docx');
              downloadButton.setAttribute('download', 'Certifications Example.docx');
              certExample.style.display = 'block';
            } else if (index === 2) {
              downloadButton.textContent = 'Sign Up Now';
              signupExample.style.display = 'block';
              downloadButton.removeAttribute('href');
              downloadButton.removeAttribute('download');
              downloadButton.onclick = function () {
                window.location.href = 'signup_mentor.php';
            };
    } 
      
            currentIndex = index;
          }
      
          formListItems.forEach((item, index) => {
            item.addEventListener('click', () => updateFormPanel(index));
          });
      
          const buttons = document.querySelectorAll('button');
          const prevBtn = buttons[0];
          const nextBtn = buttons[1];
      
          prevBtn.addEventListener('click', () => {
            if (currentIndex > 0) updateFormPanel(currentIndex - 1);
          });
      
          nextBtn.addEventListener('click', () => {
            if (currentIndex < formDetails.length - 1) updateFormPanel(currentIndex + 1);
          });
      
          updateFormPanel(0);
        </script>
